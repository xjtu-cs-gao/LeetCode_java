class Solution {
    public boolean wordBreak(String s, List<String> wordDict) {
		int n = s.length();
		boolean[] dp = new boolean[n + 1];
		dp[0] = true;
		for (int i = 1; i <= n; i++) {
			for (String word : wordDict) {   // å¯¹ç‰©å“çš„è¿­ä»£åº”è¯¥æ”¾åœ¨æœ€é‡Œå±‚
				int len = word.length();
				if (len <= i && word.equals(s.substring(i - len, i))) {
					dp[i] = dp[i] || dp[i - len];
				}
			}
		}
		return dp[n];
    }
}
